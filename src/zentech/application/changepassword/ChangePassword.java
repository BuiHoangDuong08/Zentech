    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String pass = pwdPass.getText().trim();
        Login lg = new Login();
        BCrypt_service bsv = new BCrypt_service();
        String passCheck = pass;
        if (pass.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu");
        } else {
            String password = bsv.hashPassword(pass);
            UserDAO_ChangePassword.getInstance().updatePass(this.email, password);
            UserDAO_ChangePassword.getInstance().sendOpt(passCheck, "null");
            JOptionPane.showMessageDialog(this, "Thay đổi mật khẩu thành công");
            this.dispose();
            Application.logout();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

