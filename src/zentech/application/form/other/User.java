    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // Tạo đối tượng User
            entity.User user = new entity.User(); // Đảm bảo import đúng class entity.User

            // Gán giá trị từ các trường nhập liệu
            user.setId(Integer.parseInt(jTextField8.getText().trim())); // ID
            user.setFullName(jTextField1.getText().trim());             // Full Name
            user.setUserName(jTextField2.getText().trim());             // User Name
            user.setAddress(jTextField6.getText().trim());              // Address
            user.setDob(java.sql.Date.valueOf(jTextField7.getText().trim())); // DOB (yyyy-MM-dd)
            user.setEmail(jTextField3.getText().trim());                // Email
            user.setGender(
                    jRadioButton1.isSelected() ? "Male"
                    : jRadioButton3.isSelected() ? "Female" : "None"
            ); // Gender
            user.setPhoneNumber(jTextField4.getText().trim());          // Phone
            user.setRoleId(Integer.parseInt(jTextField5.getText().trim())); // RoleId
            user.setPassword(new String(jPasswordField2.getPassword()));   // Password

            // Gọi controller để cập nhật
            controller.UserController controller = new controller.UserController();
            boolean updated = controller.updateUser(user);

            if (updated) {
                javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

